export default class Employee {
    
    id;
    username;
    phone;
    role;
    name;

    constructor() {}

    setUsername(username){
        this.username = username
    }

    setPhone(phone){
        this.phone = phone
    }

    setRole(role){
        this.role = role
    }

    setName(name){
        this.name = name
    }

}